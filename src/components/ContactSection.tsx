import { motion } from "framer-motion";
import { useInView } from "framer-motion";
import { useRef, useState } from "react";
import { Mail, MapPin, Phone, Send, Github, Linkedin, Twitter } from "lucide-react";
import { Button } from "@/components/ui/button";
import { Input } from "@/components/ui/input";
import { Textarea } from "@/components/ui/textarea";
import { useToast } from "@/hooks/use-toast";

const contactInfo = [
  { icon: Mail, label: "Email", value: "kavyaravinaik2003@gmail.com", href: "mailto:kavyaravinaik2003@gmail.com" },
  { icon: Phone, label: "Phone", value: "+91 8317379307", href: "tel:+91 8317379307" },
  { icon: MapPin, label: "Location", value: "Bengaluru, IN", href: "#" },
];

const socialLinks = [
  { icon: Github, label: "GitHub", href: "https://github.com/Phoenix-Krn" },
  { icon: Linkedin, label: "LinkedIn", href: "https://www.linkedin.com/in/kavyaravinaik/" },,
];

